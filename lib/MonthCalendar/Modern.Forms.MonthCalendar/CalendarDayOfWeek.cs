namespace Modern.Forms.MonthCalendar
{
    using System;

    /// <summary>
    /// The days of the week with combinable members - beginning with Monday.
    /// </summary>
    [Flags]
    public enum CalendarDayOfWeek
    {
        /// <summary>
        /// Indicates Monday.
        /// </summary>
        Monday = 1,

        /// <summary>
        /// Indicates Tuesday.
        /// </summary>
        Tuesday = 2,

        /// <summary>
        /// Indicates Wednesday.
        /// </summary>
        Wednesday = 4,

        /// <summary>
        /// Indicates Thursday.
        /// </summary>
        Thursday = 8,

        /// <summary>
        /// Indicates Friday.
        /// </summary>
        Friday = 16,

        /// <summary>
        /// Indicates Saturday.
        /// </summary>
        Saturday = 32,

        /// <summary>
        /// Indicates Sunday.
        /// </summary>
        Sunday = 64
    }
}