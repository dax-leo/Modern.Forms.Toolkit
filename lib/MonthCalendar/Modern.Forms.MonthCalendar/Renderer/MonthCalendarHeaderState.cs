namespace Modern.Forms.MonthCalendar.Renderer
{
    /// <summary>
    /// The header states.
    /// </summary>
    public enum MonthCalendarHeaderState
    {
        /// <summary>
        /// Indicates the default state.
        /// </summary>
        Default,

        /// <summary>
        /// Indicates the mouse over state.
        /// </summary>
        Active,

        /// <summary>
        /// Indicates the month name is in mouse over state.
        /// </summary>
        MonthNameActive,

        /// <summary>
        /// Indicates the month name is selected.
        /// </summary>
        MonthNameSelected,

        /// <summary>
        /// Indicates the year is in mouse over state.
        /// </summary>
        YearActive,

        /// <summary>
        /// Indicates the year is selected.
        /// </summary>
        YearSelected,

        /// <summary>
        /// Indicates the arrow is in mouse over state.
        /// </summary>
        ArrowActive
    }
}